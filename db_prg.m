                    find(update == u) ...
                    ) = ans(1);
                db_prg_footprint_std(...
                    find(gcs == gc), ...
                    find(threads == t), ...
                    find(elements == e), ...
                    find(update == u) ...
                    ) = ans(2);
            endfor
        endfor
    endfor
endfor
